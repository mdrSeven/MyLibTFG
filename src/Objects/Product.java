package Objects;

/**
 *
 * @author Pablo
 */
public class Product {
    String code;
    int amount;
    
    public Product(String code, int amount){
        this.code = code;
        this.amount = amount;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public int getAmount() {
        return amount;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }
    
    
}
